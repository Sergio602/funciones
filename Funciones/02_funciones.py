def area_cuadrado(L):

    area = L** 2
    return print("El area del cuadrado es:", area)

def area_rectangulo(x,y):
    area = x*y
    return print("El area del rectangulo es:", area)

def area_triangulo(b,h):
  
    area = b*h/2
    return print("El area del triangulo es:", area)


